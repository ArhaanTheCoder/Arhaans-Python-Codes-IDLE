# This tutorial teaches beginners how to get input from the user

# Ask the user for their name
name = input("What is your name? ")  # input() waits for the user to type something

# Greet the user using the name they typed
print("Hello, " + name + "!")  # prints a personalized greeting

# Optional: ask for their favorite color
color = input("What is your favorite color? ")
print("Wow! " + color + " is a nice color!")  # responds with the color
